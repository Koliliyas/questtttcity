#!/bin/bash

# –°–∫—Ä–∏–ø—Ç –¥–ª—è –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ –æ–±–ª–∞—á–Ω–æ–≥–æ —Å–µ—Ä–≤–∏—Å–∞ QuestCity
echo "üöÄ –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –æ–±–ª–∞—á–Ω–æ–≥–æ —Å–µ—Ä–≤–∏—Å–∞ QuestCity"

# –¶–≤–µ—Ç–∞ –¥–ª—è –≤—ã–≤–æ–¥–∞
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
BLUE='\033[0;34m'
NC='\033[0m'

log_info() { echo -e "${BLUE}[INFO]${NC} $1"; }
log_success() { echo -e "${GREEN}[‚úÖ OK]${NC} $1"; }
log_warning() { echo -e "${YELLOW}[‚ö†Ô∏è WARNING]${NC} $1"; }
log_error() { echo -e "${RED}[‚ùå ERROR]${NC} $1"; }

# 1. –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å—Ç–∞—Ç—É—Å–∞ —Å–µ—Ä–≤–∏—Å–æ–≤
log_info "–ü—Ä–æ–≤–µ—Ä–∫–∞ —Å—Ç–∞—Ç—É—Å–∞ —Å–µ—Ä–≤–∏—Å–æ–≤..."
if systemctl is-active --quiet nginx; then
    log_success "Nginx —Ä–∞–±–æ—Ç–∞–µ—Ç"
else
    log_error "Nginx –Ω–µ —Ä–∞–±–æ—Ç–∞–µ—Ç"
    exit 1
fi

if docker ps | grep -q questcity-backend; then
    log_success "Docker –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä —Ä–∞–±–æ—Ç–∞–µ—Ç"
else
    log_error "Docker –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä –Ω–µ —Ä–∞–±–æ—Ç–∞–µ—Ç"
    exit 1
fi

# 2. –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ nginx
log_info "–û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ nginx..."
cp /etc/nginx/sites-available/questcity-backend /etc/nginx/sites-available/questcity-backend.backup.$(date +%Y%m%d_%H%M%S)

# –°–æ–∑–¥–∞–Ω–∏–µ –Ω–æ–≤–æ–π –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏
cat > /etc/nginx/sites-available/questcity-backend << 'EOF'
server {
    listen 80;
    server_name questcity.ru www.questcity.ru 176.98.177.16;

    # –õ–æ–≥–∏
    access_log /var/log/nginx/questcity-backend.access.log;
    error_log /var/log/nginx/questcity-backend.error.log;

    # –ü—Ä–æ–∫—Å–∏—Ä–æ–≤–∞–Ω–∏–µ –Ω–∞ –±—ç–∫–µ–Ω–¥
    location / {
        proxy_pass http://172.18.0.2:8000;
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto $scheme;

        # –¢–∞–π–º–∞—É—Ç—ã
        proxy_connect_timeout 60s;
        proxy_send_timeout 60s;
        proxy_read_timeout 60s;
    }

    # –°–ø–µ—Ü–∏–∞–ª—å–Ω–∞—è –æ–±—Ä–∞–±–æ—Ç–∫–∞ –¥–ª—è API
    location /api/ {
        proxy_pass http://172.18.0.2:8000;
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto $scheme;
    }

    # Health check
    location /health {
        proxy_pass http://172.18.0.2:8000;
        proxy_set_header Host $host;
    }
}
EOF

# 3. –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ nginx
log_info "–ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ nginx..."
if nginx -t; then
    log_success "–ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è nginx –∫–æ—Ä—Ä–µ–∫—Ç–Ω–∞"
else
    log_error "–û—à–∏–±–∫–∞ –≤ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ nginx"
    exit 1
fi

# 4. –ü–µ—Ä–µ–∑–∞–≥—Ä—É–∑–∫–∞ nginx
log_info "–ü–µ—Ä–µ–∑–∞–≥—Ä—É–∑–∫–∞ nginx..."
systemctl reload nginx
if [ $? -eq 0 ]; then
    log_success "Nginx –ø–µ—Ä–µ–∑–∞–≥—Ä—É–∂–µ–Ω"
else
    log_error "–û—à–∏–±–∫–∞ –ø–µ—Ä–µ–∑–∞–≥—Ä—É–∑–∫–∏ nginx"
    exit 1
fi

# 5. –£—Å—Ç–∞–Ω–æ–≤–∫–∞ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞
log_info "–£—Å—Ç–∞–Ω–æ–≤–∫–∞ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞..."
if command -v certbot &> /dev/null; then
    log_info "Certbot —É–∂–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω"
else
    log_info "–£—Å—Ç–∞–Ω–æ–≤–∫–∞ certbot..."
    apt-get update
    apt-get install -y certbot python3-certbot-nginx
fi

# –ü–æ–ª—É—á–µ–Ω–∏–µ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞
log_info "–ü–æ–ª—É—á–µ–Ω–∏–µ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞..."
certbot --nginx -d questcity.ru -d www.questcity.ru --non-interactive --agree-tos --email admin@questcity.ru

if [ $? -eq 0 ]; then
    log_success "SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω"
else
    log_warning "–û—à–∏–±–∫–∞ —É—Å—Ç–∞–Ω–æ–≤–∫–∏ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞"
fi

# 6. –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –∞–≤—Ç–æ–æ–±–Ω–æ–≤–ª–µ–Ω–∏—è —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞
log_info "–ù–∞—Å—Ç—Ä–æ–π–∫–∞ –∞–≤—Ç–æ–æ–±–Ω–æ–≤–ª–µ–Ω–∏—è —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞..."
(crontab -l 2>/dev/null; echo "0 12 * * * /usr/bin/certbot renew --quiet") | crontab -

# 7. –§–∏–Ω–∞–ª—å–Ω–∞—è –ø—Ä–æ–≤–µ—Ä–∫–∞
log_info "–§–∏–Ω–∞–ª—å–Ω–∞—è –ø—Ä–æ–≤–µ—Ä–∫–∞..."
sleep 5

# –ü—Ä–æ–≤–µ—Ä–∫–∞ HTTP
if curl -s -f http://questcity.ru/api/v1/health/ >/dev/null 2>&1; then
    log_success "HTTP API —Ä–∞–±–æ—Ç–∞–µ—Ç"
else
    log_warning "HTTP API –Ω–µ –æ—Ç–≤–µ—á–∞–µ—Ç"
fi

# –ü—Ä–æ–≤–µ—Ä–∫–∞ HTTPS
if curl -s -f https://questcity.ru/api/v1/health/ >/dev/null 2>&1; then
    log_success "HTTPS API —Ä–∞–±–æ—Ç–∞–µ—Ç"
else
    log_warning "HTTPS API –Ω–µ –æ—Ç–≤–µ—á–∞–µ—Ç"
fi

echo ""
log_success "–ù–∞—Å—Ç—Ä–æ–π–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞!"
echo "üåê –ü—Ä–æ–≤–µ—Ä—å—Ç–µ:"
echo "   HTTP:  http://questcity.ru/api/v1/health/"
echo "   HTTPS: https://questcity.ru/api/v1/health/"

